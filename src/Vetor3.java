public class Vetor3 {

    public static void main(String[] args){

        int[] vetorInt = {1, 3, 5, 7, 9};

        System.out.println("Crie um vetor com os seguintes elementos: {1, 3, 5, 7, 9}");
        for (int i = 0; i < vetorInt.length; i++) {
            System.out.print(vetorInt[i] + " ");
        }
    }
}
